GRID = 40
t = turtle.Turtle()
t.color("white")
t.speed(0)

initialPosition()
drawC()
nextLetter()
drawT()
nextLetter()
drawR()
nextLetter()
drawL()

newLine()

drawP()
nextLetter()
drawL()
nextLetter()
drawA()
nextLetter()
