import turtle

turtle.bgcolor("orange")

def initialPosition():
    t.penup()
    t.goto(-8 * GRID, 7 * GRID)
    t.pendown()

def newLine():
    t.penup()
    t.setheading(0)
    t.goto(-8 * GRID, GRID)
    t.pendown()
    
def nextLetter():
    t.penup()
    t.setheading(0)
    t.forward(4 * GRID)
    t.pendown()

def line(size):
    t.forward(size * GRID)

def drawC():
    t.begin_fill()
    line(3)
    t.right(90)
    line(1)
    t.right(90)
    line(2)
    t.left(90)
    line(3)
    t.left(90)
    line(2)
    t.right(90)
    line(1)
    t.right(90)
    line(3)
    t.right(90)
    line(5)
    t.end_fill()

